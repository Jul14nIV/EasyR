    public void llenarTabla(Persona p) {
        this.personas.add(p);
        o[0] = p.getNombre();
        o[1] = p.getEdad();
        o[2] = p.getApellido();
        o[3] = p.getCorreo();
        o[4] = p.getHora();
        o[5] = p.getAsistencia();
        dfTable.addRow(o);
    }

    public void eliminarPersona() {
        int fila = this.table.getSelectedRow();
        if (fila >= 0) {
            this.personas.remove(fila);
            dfTable.removeRow(fila);
        }
    }

    public void ordenar() {
        table.setAutoCreateRowSorter(true);
    }

